    private void fillCmboPages() {
        
        cmboPages.removeAllItems();
        cmboPages.addItem("Select pages here");
        for (int i = 400; i <= 2000; i += 50) {
            cmboPages.addItem(String.valueOf(i));
        }
    }

   
    private void fillCmboWeight() {
        
        cmboWeight.removeAllItems();
        cmboWeight.addItem("Select book weight here");
        for (int i = 1000; i <= 2000; i += 5) {
            cmboWeight.addItem(String.valueOf(i));
        }
    }

