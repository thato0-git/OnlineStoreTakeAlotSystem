    private void btnSaveBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveBookActionPerformed
        // TODO add your handling code here:
        try{
            String title = txtTitle.getText();
            String author = txtAuthor.getText();
            String isbnNo = txtIsbnNo.getText();
            
            if(isbnNo.startsWith("0")){
                int fileSize = Integer.parseInt(txtFileSize.getText());
                bookList.add(new EBook( author,title, isbnNo,fileSize));
            }else if(isbnNo.startsWith("1")){
                String cmbOPages = (String) cmboPages.getSelectedItem();
                int noOfPages = Integer.parseInt(cmbOPages);
                
                String weight = (String) cmboWeight.getSelectedItem();
                float weightOfBook = Float.parseFloat(weight);
                
                bookList.add(new PrintBook(author, title, isbnNo, noOfPages, weightOfBook));
            }
            
             taDisplay.setText(bookList.get(bookList.size() - 1 ).toString());
             
             
        }catch (IllegalArgumentException e) {
               JOptionPane.showMessageDialog(this, e.getMessage(), "Error Message", JOptionPane.ERROR_MESSAGE);
          }
    }//GEN-LAST:event_btnSaveBookActionPerformed

    private void btnViewWeightOfBooksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewWeightOfBooksActionPerformed
        // TODO add your handling code here:
        
        taDisplay.setText(null);

          String display = "Weight of PrintBooks  \n";
          
          for(Book obj: bookList)  {          
               //snce weight is associated with printbook
               if (obj instanceof PrintBook) {
                    //display using the getsizedetails method
                    display += obj.getSizeDetails() + "\n";
               }
          }
               
          taDisplay.setText(display);
    }//GEN-LAST:event_btnViewWeightOfBooksActionPerformed

